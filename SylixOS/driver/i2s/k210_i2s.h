#ifndef __K210_I2S_H
#define __K210_I2S_H
/*********************************************************************************************************
  包含裸机库相关数据结构头文件
*********************************************************************************************************/
#include "KendryteWare/include/i2s.h"
/*********************************************************************************************************
 IOCTL 命令相关参数定义
*********************************************************************************************************/
#define IOCTL_I2S_INSTALL_DEV       (0x01)
#define IOCTL_I2S_CHAN_CONFIG       (0x02)
#define IOCTL_I2S_SET_SAMPLE_RATE   (0x04)
#define IOCTL_SINGLE_LENGTH         (0x08)
#define IOCTL_I2S_STOP              (0x10)
/*********************************************************************************************************
 I2S 设备模式设置参数
*********************************************************************************************************/
typedef struct {
    i2s_transmit_t  rxtx_mode;
    UINT32          data_channel;
    UINT32          channel_mask;
} i2s_dev_mode_t;
/*********************************************************************************************************
 I2S 设备工作配置参数
*********************************************************************************************************/
typedef struct {
    i2s_channel_num_t           channel_num;
    i2s_word_length_t           word_length;
    i2s_word_select_cycles_t    word_select_size;
    i2s_fifo_threshold_t        trigger_level;
    i2s_work_mode_t             word_mode;
} i2s_dev_config_t;
/*********************************************************************************************************
** 函数名称: k210I2sDrv
** 功能描述: 安装 I2S 驱动
** 输　入  : NONE
** 输　出  : ERROR_CODE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
INT  k210I2sDrv(VOID);
/*********************************************************************************************************
** 函数名称: k210I2sDevAdd
** 功能描述: 创建 I2S 设备
** 输　入  : uiChannel     I2S通道号
** 输　出  : ERROR_CODE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
INT  k210I2sDevAdd(UINT uiChannel);

#endif                                                                  /*        __K210_I2S_H          */
/*********************************************************************************************************
  END
*********************************************************************************************************/
